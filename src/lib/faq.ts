/**
 * Будує `schema.org/FAQPage` JSON-LD за списком запитань.
 * Винесено в чисту функцію, щоб тестувати без рендеру `.astro`.
 */
export function buildFaqJsonLd(items: readonly FaqItem[] = FAQ_ITEMS): Record<string, unknown> {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    inLanguage: "uk",
    mainEntity: items.map((item) => ({
      "@type": "Question",
      name: item.q,
      acceptedAnswer: {
        "@type": "Answer",
        text: item.a,
      },
    })),
  };
}
